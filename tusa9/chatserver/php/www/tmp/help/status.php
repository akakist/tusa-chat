<div align=justify>
Имейте ввиду, что каждый человек может настроить свои значки по своему, поэтому не лишним будет посмотреть его
статус в текстовой формулировке. Для этого нужно навести курсор на значок статуса того человека, чей статус Вы и
хотите узнать. Через мгновение, появится всплывающая надпись, где и будет отражён статус. Вы можете потренироваться,
наводя курсор на картинки выше, показанные для примера. Также каждый может индивидуально настроить надписи для
каждой картинки. Однако, когда будете сами настраивать надписи, то не старайтесь их сильно изменять, чтобы не возникало
недопонимания. И тщательно подбирайте картинки к фразам. К примеру, не ставьте надпись
"<b>Хочу поговорить</b>" на значок, который стандартно стоит на статусе "<b>Не мешайте!</b>". Это создаст огромную путаницу.<BR><BR>
Себе сменить текущий статус можно внутри чата из нижней строки (куда Вы вводите надпись). Там в последнем поле написано:
"<b>Свободен</b>" (или "<b>Свободна</b>"). Ткнув туда мышкой, откроется окошко, где Вы можете выбрать один из предложенных
статусов.<BR><BR>
Поменять надписи статусов и картинки можно в настройках чата. Войти туда можно, нажав на пункт меню
"<b>Настройки</b>" на странице, которая открывается после того, как Вы введёте свой логин и пароль
(но ещё не зайдёте в сам чат). Либо внутри чата нажать кнопку "<b>Настройки</b>".
В настройках представлена таблица. Шесть статусов уже настроено и их нельзя изменить. Но Вы можете добавить новые.
Для этого нажмите кнопку "<b>Добавить статус</b>". Появится новая строка - новый статус. Введите текст данного статуса.
Его картинку Вы можете сменить нажав на кнопку "<b>Выбрать</b>" правее статуса. А ещё правее есть крестик, нажав на который Вы
удалите этот статус.<br><br>
Имейте ввиду, что после каждого нового статуса нужно нажать кнопку "<b>Изменить</b>" внизу страницы! Если Вы сначала
настроите один статус, а потом ещё раз нажмёте "<b>Добавить статус</b>", не сохранив предыдущий, то тот первый статус аннулируется.
Поэтому, имеет смысл сначала нажить кнопку "<b>Добавить статус</b>" столько раз, сколько нужно, а потом уже изменить их все
и сохранить изменения.

