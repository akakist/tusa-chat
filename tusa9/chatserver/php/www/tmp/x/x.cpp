string unescape_rus(string& s)
{
	string out;
	for(unsigned i=0;i<s.size()-5;i++)
	{
		if(s[i]=='\%')
		{
			string repl=s.substr(i,6);
			for(int j=0;enc_repl[j];j+=2)
			{
				if(repl==enc_repl[j])
				{
				out+=enc_repl[j+1];
				i+=5;
				break;
				}
		
			}
		}
		else out+=s[i];
	}
	return out;
}
