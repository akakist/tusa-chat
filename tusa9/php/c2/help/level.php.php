4. Ваша анкета содержит нецензурную лексику, или флуд. <BR>
5. Количество сообщений в час не должно превышать 180. Если количество Ваших сообщений в час будет превышать 300 и более - Ваш логин не только не будет повышен, но ПОНИЖЕН.<br>
