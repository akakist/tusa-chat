<div class='first-line'><b><center>Что такое уровень?</center></b></div><br>

	<div class=help>



У каждого посетителя есть свой уровень, который показывает, какое этот человек занимает положение в Чате. Это поможет
хотя бы определиться, насколько "старый" посетитель (как давно в чате).
<P>Человек, строго нарушивший правила Чата или неоднократно нарушающий их, награждается уровнем "<b>Гость</b>".
 Он не может общаться в Чате в комнатах, а только по приватам.
Правила созданы для общего спокойствия, а не для того, чтобы их нарушать.
<br>


<div align=justify valign=top>
Как только Вы зарегистрировались, Вам присваивается уровень "<b>Новый Пользователь</b>". Он автоматически поднимется
до следующего уровня &quot;<b>Пользователь</b>&quot;, если Вы будете проявлять активность (значение здесь имеет количество мессаг (сообщений)
и времени проведенных в чате, а именно <i> 1 час в чате, 100 написанных сообщений и 10 заходов в чат.</i> 
Ниже следует таблица, в которой указаны уровни следующие после уровня<strong> &quot;Пользователь&quot;</strong>. 
Для поднятия уровня Вам необходимо набрать статистику, указаную в таблице. После того, как Вы её набрали, 
Вы можете встать на голосование <a href="http://www.tusovka.tomsk.ru/c2/vote/vote.php"> здесь</a>.<br>
Абсолютно всем уровни подниматься не будут! Более высокие уровни дают дополнительные привилегии, которые либо не нужны большинству пользователей, 
либо их нельзя давать слишком многим. Например, уровень Шпиона (Spy) даёт возможность смотреть IP всех присутствующих - а это мало кому нужно.
Многие просто не знают, что такое IP и как это можно использовать. Уровень Киллера даёт возможность выкидывать из чата людей. 
Нужно это для того, чтобы поддерживать порядок в чате, но представьте что будет, если каждый сможет выкинуть другого!
<br>
Поэтому, для поднятия уровня (особенно значительно!) требуются аргументы! Администраторами поощряется различная помощь, 
любая деятельность во благо Чату.
