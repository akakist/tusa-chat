<div align=justify valign=top>

Статусы нужны для того, чтобы люди могли узнать о Вашем состоянии относительно чата. Ведь они просто видят Вас в списке,
но не знают, что Вы делаете в этот момент на самом деле. Быть может, Вы ушли за чаем, или к Вам кто-то пришёл и Вы
отошли от компьютера. Просто представьте, что Вы написали кому-нибудь сообщение, а Вам не отвечают... Вы можете подумать,
что человек не хочет с Вами разговаривать, что он игнорирует Вас... а на самом деле он просто пошёл на кухню за чаем.
Такие ситуации не желательны, т.к. они могут привести к разногласиям. Для этого и нужны статусы.<BR><BR>
Если Вам нужно отойти от компьютера, то поставьте статус "<b>ушёл</b>", если Вы собираетесь оставить компьютер на
длительное время, то имеет смысл поставить статус "<b>отсутствую</b>". Также статусами можно показать, что Вы хотите
поговорить (ищите собеседника) или что Вы наоборот очень заняты и таким образом просите не мешать Вам.<BR><BR>
Для удобства, каждому статусу соответствует определённая картинка (которую Вы можете выбрать по своему желанию).
В списке присутствующих сразу после Ника стоит картинка, обозначающая статус. Если Вы, к примеру, ищите собеседника,
то Вы можете быстро пробежаться взглядом по столбцу Ников (а точнее по статусам) и выискать значок, обозначающий статус
"<b>хочу поговорить</b>". Или Вы очень просто можете узнать, присутствует ли реально тот человек, с которым Вы хотите
поговорить. Для этого нужно просто глянуть на его статус (на картинку рядом с его Ником). Вот картинки стандартных
обозначений статусов:<P>
