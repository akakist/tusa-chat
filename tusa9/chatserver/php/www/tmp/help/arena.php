              <p>Для этого Вам нужно написать <b>в приват</b> игровому боту (ник <b><span lang="en-us">Arena</span></b>) "<font color="#FFCC00">!reg &lt;раса&gt; &lt;профессия&gt;</font>".</p>

              <p>Вместо &lt;раса&gt; Вы должны подставить ту расу, которой вы хотите играть. Каждая раса имеет свои бонусы. Вот список всех игровых рас:</p>

              <p><b>Список игровых рас:</b><br>
              <font color="#FFCC00">(l) Ящер str/+1 dex/+2<br>
              (r) Тритон dex/+2 con/+1<br>
              (g) Гном str/+1 con/+2<br>
              (f) Хоббит dex/+2 int/+1<br>
              (e) Эльф dex/+1 int/+2<br>
              (d) Дроу wis/+2 int/+1<br>
              (h) Человек wis/+1 int/+2<br>
              (o) Орк str/+2 dex/+1<br>
              (t) Тролль str/+2 con/+1</font></p>

              <p align="justify">В скобках указывается индекс этой расы, который Вы и должны подставить вместо &lt;раса&gt;. Вообще, все индексы в игре пишутся только строчными латинскими буквами.
              После названия расы указываются бонусы (первичные характеристики), которые игрок получает каждый нечётный уровень, играя этой расой. Вот что значит каждое сокращение названия
              бонуса:</p>

