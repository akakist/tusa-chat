<div class='first-line'>Правила пользования чатом</div>

<div class=help>

	<p align="justify"><b>Настоящие правила вступают в силу с момента их опубликования и регулируют основные положения в Чате 
	&quot;Тусовка&quot;. Настоящий документ обязателен к неукоснительному соблюдению всеми участниками Чата.</b> </p>

	<div align="justify" valign="top"> <p align="center"><br> <b>Наказуемые деяния: </b></p>

	<ol class=olh> <li class=olh><b> 
	Мат (псевдомат) в общем фрейме.</b> В Чате запрещено ругаться матом (и его производными), оскорблять присутствующих. В Чат приходят пообщаться,
	а не устраивать словесные поединки. Ругань допускается только в комнате "Помойка", свободной от администрации. </li>

	<li><b> Оскорбления и ругань.</b> <br>
	Запрещены негативные изречения, изначально направленные на ущемление чести и достоинства присутствующих, к одному человеку или ко всему Чату,
	в целом, в общем фрейме. </li>
	
	<li><b> Пропаганда.</b><br>
	В Чате запрещена пропаганда насилия, порнографии, наркотиков  и алкогольных напитков.</li>
	
	<li><b>Плагиатные, нецензурные, оскорбительные, перевыделенные символами ники.</b> <br>
        <b>4.1.</b> Плагиатным считается ник, дублирующий уже существующий. Это доставляет неудобства посетителям и хозяину ника. 
        Будьте оригинальны - придумайте свой ник.<br>
	<b>4.2.</b> Перевыделенным считается ник, в котором  содержится более трёх символов (цифр) подряд, с двух или одной стороны.<br>
	<b>Пояснение:</b><br>
	<b>А_Л_Е_К_С_А_Н_Р</b> - за такой ник Вы будете выставлены из Чата.<br>
	<b>7777ВАСЯ7777</b> -  за такой ник Вы будете выставлены из Чата.<br>
	<b>ВАСЯ______Я</b> - за такой ник Вы будете выставлены из Чата.<br>
	<b>Anggggelina</b> - за такой ник Вы будете выставлены из Чата.<br></p>
	<b>4.3.</b> Запрещены ники однотипные статусам Администрации и Модераторов, если Вы таковым не являетесь. 
	Это может предоставить неудобство пользователям. Такие ники удаляются в обязательном порядке, а в случае отказа - изымаются.<br>
	<b>4.4.</b> Запрещены ники, пропагандирующие наркотики, употребление в нике мата (псевдомата) или сексуально неадекватные ники. </li>

	<li><b> Флуд, Зелень и Многострочие.<br>
	5.1. ФЛУД.</b>  Это многократное употребление слов, фраз, цифр или символов, которые мешают общаться другим пользователям.<br>
	<b>Пояснение:<br></b>
		<b>Вася: ВСЕМ ПРИВЕТ !!!!<br>
		Вася: ВСЕМ ПРИВЕТ !!!!<br>
		Вася: ВСЕМ ПРИВЕТ !!!!<br>
		Вася: ВСЕМ ПРИВЕТ !!!!<br></b>
	<b>5.2.</b> Также флудом считается многократное употребление «Действий», то есть написание своих сообщений зеленым текстом (Зелень). 
	За злоупотребление (более 4-х раз в минуту) Вы будете выставлены из Чата.<br>
	<b>5.3 Многострочие</b> - изложение законченной фразы более чем на трех строках.<br>
	<b>Пояснение:</b><br>
	<b>Вася: Давай<br>
	Вася: пойдем<br>
	Вася: с тобой<br>
	Вася: в кино?</b><br>
	За набор подобных сообщений Вы будете выставлены из Чата. Выражайте свои мысли в одну строку.</li>
	
	<li><b> Выделение, Caps Lock, Цвета и Транслит.</b><br>
	<b>6.1.</b> Злоупотребление клавишей Caps Lock, и ПеРеМеНнЫм регистром. К злоупотреблению верхним регистром не относится написание
	отдельных слов или предложений заглавными буквами для подчеркивания какой-то мысли или конкретного слова.<br>
	<b>Пояснение:<br></b>
	<b>Вася: _______________Всем привет _____________________</b> (за такое сообщение Вы будете выставлены из Чата)<br>
	<b>Вася: ВСЕМ ПРИВЕТ !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!</b> (за такое сообщение Вы будете выставлены из Чата)<br>
	<b>Вася: ПрИвЕт ЧеМ ЗаНяТ ?</b> (за такое сообщение Вы будете выставлены из Чата)</p><br>
	<b>6.2.</b> Запрещены многоцветные сообщения при помощи Irc.<br>
	<b>6.3.</b> Чат поддерживает русский язык, поэтому сообщения транслитом будут пресекаться<br>
	<b>Пояснение:</b><br>
	<b>Вася: privet wsem kto tyt jiwoy ????</b> (за такое сообщение Вы будете выставлены из Чата)</li>

	<li><b> Реклама.</b><br>
	Любая реклама, выдаваемая в общий фрейм, а также массово в приват, ЗАПРЕЩЕНА! Но никто не запрещает закинуть в чат ссылку на 
	известный развлекательный, музыкальный, или информационный ресурс.
	По всем вопросам размещения рекламы в Чате следует обращаться на <b>Support</b>.</li>

	<li><b> Провокации и беспорядки.</b><br>
	Не пытайтесь оскорблять Модераторов в Чате. Закончится всё равно не в Вашу пользу. Они делают то, что должны, а именно, требуют 
	от пользователей соблюдения вот этих нехитрых правил, которые ко всему прочему, написаны не ими. Вы имеете право спросить 
	(в мягкой и вежливой форме) у Модератора, за что были удалены, если Вам это не ясно. На что он обязан Вам это кратко объяснить.
	 Это для того, чтобы Вы не совершали ошибок впоследствии.</li>
	<li><b> Спам.</b><br>
	Спамом является рассылка записок (ссылок), так называемых "Писем счастья", целью которых является призвать пользователей к многократной 
	пересылке копий этих записок или ссылок. Если вам пришла такая записка, вы можете обратиться к Модератору за помощью. Также запрещается 
	рассылка ссылок не несущих смысловой нагрузки и с просьбой проголосовать за того или иного кандидата.<br></li>
	
	<li><b> IRC. Музыка и Боты</b><br>
	Вы можете пользоваться IRC-клиентом для удобства общения в Чате, но при этом вы должны соблюдать следующие правила:<br>
	<b>10.1. Музыкальные скрипты.</b><br>
	Если Вы хотите кому-либо сообщить, что Вы слушаете в данный момент, то сделайте это приватно. Остальным пользователям это просто неинтересно, 
	зато вызывает зафлуживание общего фрейма. За рецидивы использования таких скриптов Вы можете надолго лишиться доступа на канал. Но никто 
	не запрещает собственноручное написание название группы и песни, которую Вы слущаете.<br>
	<b>10.2. Использование irc-ботов.</b><br>
	В Чате запрещены самовольные (не согласованные с Администрацией) irc-боты любого направления. Можно договориться с Администрацией
	на размещение такого бота, описав его функции и нужность. Вопросы и краткое изложение функций бота отправляйте запиской на ник <b>Support</b>.</li>
<br>
