<BR><br>Уровень <b>Чаттер</b> - Это пользователь со стажем. Ему разрешается использовать тег для выделения текста курсивом.

<br>Уровень <b>Тусовщик</b> - Человек, который тусуется преимущественно в нашем чате, а так же был хоть на одном коннекте.<BR><BR>

Уровень <b>VIP</b> - очень важная персона. Пользуется доверием Администрации Чата, большинства постоянных пользователей. На данный уровень не особо влияет статистика, основной фактор для повышения до такового - знакомство и только оно. Посещайте коннекты, ведите активный образ жизни и Вы непременно добьётесь данного уровня. Побочные уровни, приравненные к <b>VIP</b>, должны иметь особое внушительное обоснование, отражающее действительность относительно уровня. <br>Уровни <b>Киллер</b> и <b>Страж</b> не рассматриваются по статистике и вовсе. Для того чтобы получить данные уровни нужно часто находиться в чате и пользоваться доверием Администраторов (либо большинства постоянных посетителей).<br>
<br>

Повышение до уровней: <b>Шпион</b>, <b>Админ</b> практически не рассматривается... для этого нужны особые причины (например, помощь в исправлении весомых дырок в чате).<br>Так же обязательным фактором при получении уровней <b>"Киллер", "Страж", "Шпион", "Админ"</b> является совершеннолетие.

