              dex - Ловкость<br>
              wis - Мудрость<br>
              int - Интеллект<br>
              con - Телосложение</font><br>
              <br>
              Далее идет &lt;профессия&gt;, туда Вы ставите индекс желаемой профессии.</p>

              <p align="justify"><b>Список профессий:</b><br>
