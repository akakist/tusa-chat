<div class='first-line'>Особенности нашего чата</div>

<div class=help>
<p>

                  Для того, чтобы Вам легче было принять решение о том, нравится ли Вам чат, мы решили перечислить все особенности нашего чата. Вы можете сравнить с любым другим известным Вам чатом и
                  сделать выбор какой чат лучше и в чём.<br>
                  <br>

                  <ul>
                    <li><b>IRC-режим</b></li>

                    <li style="list-style: none"><br>
                    Этот режим работы чата, когда страница с сообщениями не обновляется с определённым интервалом времени, а каждое новое сообщение сразу появляется без задержек - т.е. оно начнёт
                    передаваться каждому сразу же, как только оно будет отправлено. Таким образом, при хорошей связи, Ваше сообщение все остальные увидят через 0,1-1 секунды после отправки. (в
                    зависимости от скорости Вашего соединения)</li>

                    <li><b>IRC клиенты</b></li>

                    <li style="list-style: none"><br>
                    Наш чат поддерживает соединение по протоколу IRC. Это означает что в чате Вы можете сидеть не только с WEBа, но и клиентскими программами, например - mIRC</a>.<br>
                   </a>.</li>

                    <li><b>Функция "Позвать"</b></li>

                    <li style="list-style: none"><br>
                    Бывает, что человек временно сворачивает окно с чатом и не видит всё, что ему пишут. При использовании этой функции, у человека, которого Вы зовёте, выскочит маленькое окошко с
                    текстом, который Вы сами напишите.</li>

                    <li><b>Поддержка статусов</b></li>

                    <li style="list-style: none"><br>
                    Обычно в списке присутствующих в чате видно только Ники (псевдонимы) участников, но не все из них принимают активное участие в беседах, а некоторых и вовсе нет за компьютером. У
                    нас в чате каждый участник может поставить себе статус. Например, "<b>Отошёл</b>" или "<b>Не мешайте</b>". Тогда рядом с Ником этого человека изменится картинка, символизирующая
                    статус, а текстовое обозначение статуса можно будет прочитать, если задержать курсор мышки над картинкой статуса (всплывёт подсказка). Кроме того, картинки и текстовое обозначение
                    статусов каждый может настроить по своему вкусу.</li>

                    <li><b>Уведомление о неактивности пользователя</b></li>

                    <li style="list-style: none"><br>
                    Если кто-либо не будет проявлять активность в чате в течении 5 минут, либо в течении этого же времени будет присутствовать в другом окне, то у этого пользователя выставится статус
                    "Отошёл". Проявлением активности считается даже движение мышки. Таким образом, этот статус ставится у того, кто либо вообще отошёл от компьютера, либо у того, кто в течении
                    длительного времени находился в другом окне и не открывал окно чата.</li>

                    <li><b>Различные уровни</b></li>

                    <li style="list-style: none"><br>
                    У нас в чате, кроме обычных пользователей есть не только Администраторы, но и много (около 10) других уровней пользователей. Каждый из этих уровней даёт какие-нибудь
                    дополнительные права. Например, использование HTML-тегов или возможность выкинуть кого-нибудь из чата.</li>

                    <li><b>Несколько Ников</b></li>

                    <li style="list-style: none"><br>
                    Если Вы хотите сидеть в чате под разными именами, то Вам не обязательно регистрироваться несколько раз. Вы регистрируетесь всего один раз, а потом делаете себе несколько Ников
                    (псевдонимов). И при входе в чат Вам будет предложено выбрать один из зарегистрированных Ников.</li>

                    <li><b>Несколько фоток</b></li>

                    <li style="list-style: none"><br>
                    Каждый зарегистрированный пользователь может вставлять в свою инфу несколько фотографий и к каждой из них писать отдельное описание. Таким образом, каждый может создать свою
                    собственную галерею.</li>

                    <li><b>Записки</b></li>

                    <li style="list-style: none"><br>
                    Если какого-то человека нет сейчас в чате, а Вам ему нужно что-то передать, то Вы можете оставить ему записку. При следующем входе в чат он её получит.</li>

                    <li><b>Возможность передачи файлов</b></li>

                    <li style="list-style: none"><br>
                    Теперь в чате Вы можете передать другому человеку какой-нибудь файл (например, фотографию). Для этого Вам нужно будет отправить записку, к которой Вы сможете прикрепить этот
                    файл.</li>

                    <li><b>Голосование</b></li>

                    <li style="list-style: none"><br>
                    Повышения до высокого уровня в чате рассматривается на голосовании. Голосующим может быть любой объективный, зарегистрированный пользователь чата.</li>

                    <li><b>Форум</b></li>

                    <li style="list-style: none"><br>
                    В качестве оффлайнового общения у нас в чате есть простой и удобный форум, где каждый зарегистрированный пользователь может высказатся на определенную тему, или создать свою.
                    Незарегистрированые пользователи имеют возможность читать форум.</li>
                  </ul>

