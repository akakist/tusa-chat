	    наказания может и увеличиться.<br></li><br>
	<li><b> Топики или за что Вы можете лишиться уровня.</b><br> 
						<b>12.1.</b>  За установку топика содержащий нецензурную лексику, оскорбления (унижения)<br>
						и т.д. Взависимости от степени тяжести, Вы можете получить наказание в виде: <br>
						1. Предупреждение с выставлением из Чата Тусовка.<br>
						2. бан от 2х дней до 1 недели.<br>
						3. Понижение уровня. <br>
	<b>12.2.</b> Также если Вы злостный нарушитель и Вы не реагируете на предупреждения (наказание в виде бана от 2х дней до 4х недель), 
	Администрация вправе понизить Вас (разжаловать) до уровня гость, после чего Вы не сможете больше общаться в общий фрейм. Повысить
	же себе уровень будет очень накладно.<br>
        <b>Исключение</b>: <i>Помойка</i> - канал, свободный от Администрации. Здесь можно всё (кроме Рекламы и Спама). Только учтите, что время и количество 
        сообщений на данном канале в статистику не засчитывается.</li></b>
<br><li>Форум.<br>
<b>На форуме запрещается:</b><br>
<b>13.1</b> Маты, как в теме, так и в самом сообщении. Также запрещены грубые высказывания, оскорбляющие как форумчан, так и модераторов. В противном случае модератор имеет право удалить Ваше сообщение, либо забанить Вас от 20 минут до 2 дней при рецедивах.<br>
<b>13.2</b> Поднимать старые темы, которые неактуальны, если Ваше сообщение не несет никакой важной информации. Исключение: темы с поздравлением именинников. Актуальными темами являются предложения \ помощь по Чату, основные вопросы по Чату.<br>
<b>13.3</b> Дублировать свои сообщения по нескольку раз. Это будет расцениваться как флуд и модератор имеет право дать Вам бан от 20 минут до 2 дней.<br>
<b>13.4</b> Создавать темы, содержащие какую либо рекламу. Это приравневается к спаму. При первом нарушении модератор имеет право удалить Вашу тему и дать Вам предупреждение. При рецедиве дается бан от 2 дней до 2 недель. Особо отличившиеся спамеры могут быть награждены уровнем Гость.<br>
<b>13.5</b> Писать так называемые "письма счастья" как в самой теме, так и в простом сообщении. За это нарушение предусмотрен бан от 1 до 2 недель.<br>
<b>13.6</b> Запрещено давать неинформативные комментарии, либо флудить в темах. Модератор в праве удалить Ваше сообщение, либо выдать бан от 20 минут до 2 дней при рецедиве.<br>Форумчане, <b>помните</b>, что каждое Ваше нарушение учитывается и особо отличившиеся нарушители могут быть удостоены уровнем Гость!<br><br>
    <b>Действия Модератора могут быть обжалованы. В случае признания претензий правомерными, к Модератору будут применены санкции. 
    Жалобы рекомендуется направлять запиской на ник Support, с указанием конкретного случая, и приложением скриншота или лога.</br> 
						
    В заключение: Модерирование придумано не для того, чтобы лишать людей возможности говорить, а для того, чтобы помочь им общаться в спокойной обстановке.<br>

